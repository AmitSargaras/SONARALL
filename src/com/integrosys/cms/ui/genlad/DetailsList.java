package com.integrosys.cms.ui.genlad;

public class DetailsList {

	public String sn = null;
	public String facilityName = null;
	public String limitInForce = null;
	public String outstandingAmount = null;
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
	public String getFacilityName() {
		return facilityName;
	}
	public void setFacilityName(String facilityName) {
		this.facilityName = facilityName;
	}
	public String getLimitInForce() {
		return limitInForce;
	}
	public void setLimitInForce(String limitInForce) {
		this.limitInForce = limitInForce;
	}
	public String getOutstandingAmount() {
		return outstandingAmount;
	}
	public void setOutstandingAmount(String outstandingAmount) {
		this.outstandingAmount = outstandingAmount;
	}
	
	
	
	
}
