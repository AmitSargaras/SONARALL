package com.aurionpro.clims.rest.dto;

public class UdfRestRequestDTO {

	private String moduleId;
	private String udfLabel;
	private String udfSequance;
	private String udfValue;
	
	public String getModuleId() {
		return moduleId;
	}
	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}
	public String getUdfLabel() {
		return udfLabel;
	}
	public void setUdfLabel(String udfLabel) {
		this.udfLabel = udfLabel;
	}
	public String getUdfSequance() {
		return udfSequance;
	}
	public void setUdfSequance(String udfSequance) {
		this.udfSequance = udfSequance;
	}
	public String getUdfValue() {
		return udfValue;
	}
	public void setUdfValue(String udfValue) {
		this.udfValue = udfValue;
	}

	
}
